#include <stdio.h>

int main(void){
	int firstNo, secondNo;

	firstNo = 5;
	secondNo = 2;
	printf("\n%d + %d = %d", firstNo, secondNo, firstNo+secondNo);
	printf("\n%d * %d = %d", firstNo, secondNo, firstNo+secondNo);
	printf("\n%d - %d = %d", firstNo, secondNo, firstNo-secondNo);
	printf("\n%d / %d = %f\n", firstNo, secondNo, firstNo/(float)secondNo);
	return 0;
} 
